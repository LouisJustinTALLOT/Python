def countApplesAndOranges(s, t, a, b, apples, oranges):
    res_oranges = 0
    res_apples = 0

    for apple in apples:
        landing_point = a + apple
        if s <= landing_point and landing_point <= t:
            res_apples += 1

    for orange in oranges:
        landing_point = b + orange
        if s <= landing_point and landing_point <= t:
            res_oranges += 1       

    print(res_apples)
    print(res_oranges)

